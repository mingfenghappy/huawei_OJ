package swichname1;

public class switchname1 {
	
	  public static void main(String[] args)
	  {
	    String a = "夏天";
		switch(a)
		{
		case "春天":
		     System.out.println("当春乃发生");break;
	    case "夏天":
		     System.out.println("盛夏的果实");break;
		case "秋天":
		     System.out.println("秋收的季节");break;
		case "冬天":
		     System.out.println("冬天的威尼斯");break;
		default:
		     System.out.println("都不爱");
		}
	  
	}
}
